let current = (typeof localStorage !== "undefined" && localStorage.getItem("mars3d.lang")) || "en";
if (!DICT[current]) current = "en";
const listeners = new Set();

export function t(key) {
  return DICT[current][key] ?? DICT.en[key] ?? key;
}

export function getLang() {
  return current;
}

export function setLang(lang) {
  if (!DICT[lang] || lang === current) return;
  current = lang;
  if (typeof localStorage !== "undefined") localStorage.setItem("mars3d.lang", lang);
  applyToDom();
  listeners.forEach((fn) => fn(lang));
}

export function onLangChange(fn) {
  listeners.add(fn);
  return () => listeners.delete(fn);
}

export function applyToDom(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    const spec = el.dataset.i18nAttr;
    spec.split(";").forEach((pair) => {
      const [attr, key] = pair.split(":").map((s) => s.trim());
      if (attr && key) el.setAttribute(attr, t(key));
    });
  });
}

export function localeForNumbers() {
  return current === "es" ? "es-ES" : "en-US";
}
